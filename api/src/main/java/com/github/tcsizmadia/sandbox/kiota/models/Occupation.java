package com.github.tcsizmadia.sandbox.kiota.models;

/**
 * Represents the occupation of a person.
 *
 * @author Tamas Csizmadia
 * @see Person
 */
public enum Occupation {
    /**
     * The person is a writer.
     */
    WRITER,
    /**
     * The person is an actor.
     */
    ACTOR,
    /**
     * The person is a musician.
     */
    MUSICIAN,
    /**
     * The person is a politician.
     */
    POLITICIAN,
    /**
     * The person is a scientist.
     */
    SCIENTIST,
    /**
     * The person is an athlete.
     */
    ATHLETE
}
